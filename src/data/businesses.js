export const getByCategory = (categoryId) =>
  businesses.filter((b) => b.category === categoryId);

export const getTopRanked = (limit = 5) =>
  [...businesses].sort((a, b) => b.rating - a.rating || b.reviews - a.reviews).slice(0, limit);

export const getFeatured = () => businesses.filter((b) => b.featured);

export const searchBusinesses = (query, category) => {
  const q = query.toLowerCase();
  return businesses.filter((b) => {
    const matchQuery =
      !q ||
      b.name.toLowerCase().includes(q) ||
      b.description.toLowerCase().includes(q) ||
      b.tags.some((t) => t.toLowerCase().includes(q));
    const matchCat = !category || b.category === category;
    return matchQuery && matchCat;
  });
};
